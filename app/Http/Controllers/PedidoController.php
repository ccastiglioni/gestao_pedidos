     */
    public function store(Request $request)
    {
        $obPed = new Pedido();
        $obPed->nome       = $request->get('nome');
        $obPed->cliente_id = $request->get('cliente_id');
        $obPed->save();
        return redirect()->route('pedido.index');
    }

